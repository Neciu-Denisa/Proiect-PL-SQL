CREATE TABLE ACTOR 
    ( 
     ACT_ID NUMBER (6)  NOT NULL , 
     ACT_FNAME CHAR (20 BYTE)  NOT NULL , 
     ACT_LNAME CHAR (20 BYTE)  NOT NULL , 
     ACT_GENDER CHAR (1 BYTE) 
    );


CREATE UNIQUE INDEX ACTOR_PK ON ACTOR 
    ( 
     ACT_ID ASC 
    );

ALTER TABLE ACTOR 
    ADD CONSTRAINT ACTOR_PK PRIMARY KEY ( ACT_ID ) 
    USING INDEX ACTOR_PK ;



